<template>
  <div>
    <router-view></router-view>

    <!--서비스 로케이터 리스트-->
    <service-locator :host="backendUrl" path="/" resource-name="backend"></service-locator>

    <!--글로벌 알림 컴포넌트-->
    <md-snackbar md-position="top right" ref="snackbar" :md-duration="4000">
      <span class="md-primary">{{snackbar.text}}</span>
      <md-button class="md-accent" md-theme="light-blue" @click="$refs.snackbar.close()">Close</md-button>
    </md-snackbar>

    <confirm
      title="Are you sure?"
      content-html="사용자를 삭제합니다."
      ok-text="사용자 삭제"
      cancel-text="취소"
      ref="confirm"
    ></confirm>
  </div>
</template>
<script>
  export default {
    props: {},
    data () {
      return {
        backendUrl: window.backendUrl,
        snackbar: {
          top: true,
          right: true,
          timeout: 6000,
          trigger: false,
          mode: 'multi-line',
          context: 'info',
          text: ''
        },
      }
    },
    mounted() {

    },
    methods: {
      info: function (msg) {
        this.snackbar.context = 'info';
        this.snackbar.text = msg;
        this.$refs.snackbar.open();
      },
      error: function (msg) {
        this.snackbar.context = 'error';
        this.snackbar.text = msg;
        this.$refs.snackbar.open();
      },
      warning: function (msg) {
        this.snackbar.context = 'warning';
        this.snackbar.text = msg;
        this.$refs.snackbar.open();
      },
      success: function (msg) {
        this.snackbar.context = 'success';
        this.snackbar.text = msg;
        this.$refs.snackbar.open();
      },
      confirm: function (options) {
        this.$refs.confirm.open(options);
      }
    }
  }
</script>

